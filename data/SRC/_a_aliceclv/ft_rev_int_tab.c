void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int cp;

	i = 0;
	j = size - 1;
	while (size > 1)
	{
		cp = *(tab + i);
		*(tab + i) = *(tab + (j - i));
		*(tab + (j - i)) = cp;
		size = size / 2;
		i++;
	}
}
