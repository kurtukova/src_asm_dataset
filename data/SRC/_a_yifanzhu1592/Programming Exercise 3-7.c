// There are 2.54 centimeters to the inch. Write a program that asks you to enter your
// height in inches and then displays your height in centimeters. Or, if you prefer, ask for
// the height in centimeters and converts that to inches.


#include <stdio.h>

int main(void)
{
	float height_centimeters, height_inches;
	float centimeters_to_inch = 2.54;
	
	printf("Please enter your height in centimeters:\n");
	scanf("%f", &height_centimeters);
	height_inches = height_centimeters / centimeters_to_inch;
	printf("Your height in inches is %f", height_inches);
	
	return 0;
}
